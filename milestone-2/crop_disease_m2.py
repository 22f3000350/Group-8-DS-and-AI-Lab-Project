"""---
## 11. 🔄 Augmentation of Minority Classes <a id='11'></a>

**Applied only to training images.** Val and test sets are never augmented.

**Transforms:** Horizontal flip, Vertical flip, Rotation (90°/180°/270°), Brightness (±30%), Contrast (±20%), Saturation (±20%), Zoom crop (±15%).
"""

def augment_image(img):
    # Horizontal flip
    if random.random() > 0.5:
        img = img.transpose(Image.FLIP_LEFT_RIGHT)
    # Vertical flip
    if random.random() > 0.5:
        img = img.transpose(Image.FLIP_TOP_BOTTOM)
    # Rotation
    angle = random.choice([0, 90, 180, 270])
    if angle: img = img.rotate(angle, expand=False)
    # Brightness
    img = ImageEnhance.Brightness(img).enhance(random.uniform(0.7, 1.3))
    # Contrast
    img = ImageEnhance.Contrast(img).enhance(random.uniform(0.8, 1.2))
    # Saturation
    img = ImageEnhance.Color(img).enhance(random.uniform(0.8, 1.2))
    # Zoom (crop center and resize back)
    if random.random() > 0.5:
        w, h = img.size
        zoom = random.uniform(0.85, 1.0)
        left = int(w * (1 - zoom) / 2); top = int(h * (1 - zoom) / 2)
        img = img.crop((left, top, w - left, h - top)).resize((w, h), Image.LANCZOS)
    return img

# Preview on a Sugarcane image
sc_files = [fp for fp, cls in splits['train'] if 'Sugarcane' in cls or 'sugarcane' in cls.lower()]
