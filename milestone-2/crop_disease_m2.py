"""---
# PART B — Preprocessing Pipeline
---
> All variables (`df`, `class_folders`, `crop_colors`, etc.) carry forward directly from Part A. No re-scanning needed.

---
## 8. 🔍 Near-Duplicate Removal (Full Dataset) <a id='8'></a>

EDA sampled 500 images and found near-duplicates. Here we run pHash on the **full dataset** and remove all near-duplicate images before any splitting, to prevent data leakage between train and test.
"""

# Step 1: Remove exact duplicates first
print('Step 1 — Removing exact duplicates (MD5)...')
before = len(df)
df = df.drop_duplicates(subset='md5_hash', keep='first').reset_index(drop=True)
print(f'   Removed : {before - len(df)} exact duplicates')
print(f'   Remaining: {len(df)} images')

# Step 2: Full-dataset pHash near-duplicate removal
print('Step 2 — Full pHash near-duplicate removal...')
print('   (This may take 5–10 minutes)')

phash_map = {}
for fp in tqdm(df['filepath'], desc='pHashing all images'):
    try:
        phash_map[fp] = imagehash.phash(Image.open(fp).convert('RGB'))
    except:
        phash_map[fp] = None

fps_all = list(phash_map.keys())
to_remove = set()
removed_pairs = []

for i in tqdm(range(len(fps_all)), desc='Comparing hashes'):
    if fps_all[i] in to_remove or phash_map[fps_all[i]] is None:
        continue
    for j in range(i + 1, len(fps_all)):
        if fps_all[j] in to_remove or phash_map[fps_all[j]] is None:
            continue
        if phash_map[fps_all[i]] - phash_map[fps_all[j]] <= 5:
            to_remove.add(fps_all[j])
            removed_pairs.append((os.path.basename(fps_all[i]), os.path.basename(fps_all[j])))

before = len(df)
df = df[~df['filepath'].isin(to_remove)].reset_index(drop=True)
print(f'\n   Near-duplicate pairs found  : {len(removed_pairs)}')
print(f'   Images removed              : {before - len(df)}')
print(f'   Images remaining            : {len(df)}')
for a, b in removed_pairs:
    print(f'   KEPT: {a}  |  REMOVED: {b}')

"""---
## 9. 🖼️ Resize & RGB Conversion <a id='9'></a>

**Why 224×224:** Images range from 16px to 6,000px. All CNN architectures require a fixed uniform input size. 224×224 is the ImageNet standard, directly compatible with ResNet, EfficientNet, VGG, and MobileNet pretrained weights.

**Why LANCZOS:** Highest quality resampling filter for downscaling — preserves fine leaf texture detail better than bilinear or nearest-neighbour.
"""

TEMP_RESIZED = '/content/resized_temp'
os.makedirs(TEMP_RESIZED, exist_ok=True)
resize_errors = []
resized_records = []

print(f'Resizing all {len(df)} images to {TARGET_SIZE} and converting to RGB...')

for _, row in tqdm(df.iterrows(), total=len(df), desc='Resizing'):
    try:
        img = Image.open(row['filepath']).convert('RGB').resize(TARGET_SIZE, Image.LANCZOS)
        out_dir = os.path.join(TEMP_RESIZED, row['class'])
        os.makedirs(out_dir, exist_ok=True)
        # Keep original extension name but save as JPEG
        out_fname = os.path.splitext(row['filename'])[0] + '.jpg'
        out_path  = os.path.join(out_dir, out_fname)
        img.save(out_path, 'JPEG', quality=95)
        resized_records.append({'filepath': out_path, 'filename': out_fname,
                                 'class': row['class'], 'crop': row['crop']})
    except Exception as e:
        resize_errors.append({'file': row['filepath'], 'error': str(e)})

df_resized = pd.DataFrame(resized_records)
print(f'\n✅ Resize complete.')
print(f'   Successfully resized : {len(df_resized)}')
