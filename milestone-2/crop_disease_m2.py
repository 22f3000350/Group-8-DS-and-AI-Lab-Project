# Per-class mean RGB (sampled)
def compute_mean_rgb(filepaths, sample_size=100, target=(128, 128)):
    sampled = np.random.choice(filepaths, min(sample_size, len(filepaths)), replace=False)
    r_vals, g_vals, b_vals = [], [], []
    for fp in sampled:
        try:
            arr = np.array(Image.open(fp).convert('RGB').resize(target)) / 255.0
            r_vals.append(arr[:,:,0].mean())
            g_vals.append(arr[:,:,1].mean())
            b_vals.append(arr[:,:,2].mean())
        except: continue
    return {'mean_R': np.mean(r_vals), 'mean_G': np.mean(g_vals), 'mean_B': np.mean(b_vals)}

